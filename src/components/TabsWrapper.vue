<template>
  <div class="tabs">
    <ul
      class="flex flex-wrap justify-around text-sm font-medium text-center mb-5 border-b-2 text-gray-500 dark:text-gray-400"
    >
      <li
        v-for="title in tabTitles"
        :key="title"
        :class="{ 'active-tab': title == selectedTitle }"
        @click="selectedTitle = title"
        class="w-24 inline-block py-3 px-4 rounded-lg hover:text-gray-900 hover:bg-gray-100 dark:hover:bg-gray-800 dark:hover:text-white mr-2 last:mr-0 mb-3 cursor-pointer select-none"
      >
        {{ title }}
      </li>
    </ul>
    <div class="flex justify-center">
      <input
        type="text"
        class="w-24 h-5 border border-gray-300 text-gray-900 text-sm text-center opacity-50 mb-3 rounded-lg"
        value="#123"
        disabled
      />
    </div>
    <slot />
  </div>
</template>

<script setup>
import { useSlots, ref, provide } from "vue";
const slots = useSlots();
const props = defineProps();

const tabTitles = ref(slots.default().map((tab) => tab.props.title));
const selectedTitle = ref(tabTitles.value[0]);

provide("selectedTitle", selectedTitle);
</script>

<style scoped>
.active-tab {
  @apply inline-block py-3 px-4 text-white bg-red-500 rounded-lg;
}
</style>
